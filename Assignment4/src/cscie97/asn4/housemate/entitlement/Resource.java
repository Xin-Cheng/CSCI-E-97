package cscie97.asn4.housemate.entitlement;

public class Resource {
	private String id;
	private String name;
	
	public Resource() {
		
	}
	
	public void setID(String id) {
		this.id = id;
	}
	public String getID(){
		return id;
	}
	
	public void setName(String name){
		this.name = name;
	}
	public String getName(){
		return name;
	}
}
